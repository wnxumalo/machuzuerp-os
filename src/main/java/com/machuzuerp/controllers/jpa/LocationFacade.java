package com.machuzuerp.controllers.jpa;

import com.machuzuerp.entities.Cities;
import com.machuzuerp.entities.Location;
import com.machuzuerp.entities.Payment;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

/**
 *
 * @author Wandile
 */
@Stateless
public class LocationFacade extends AbstractFacade<Location> {

    @PersistenceContext(unitName = "MachuzuERPPU")
    private EntityManager em;

    @Override
    protected EntityManager getEntityManager() {
        return em;
    }

    public LocationFacade() {
        super(Location.class);
    } 
    
    public List<Location> getSupplierLocations(Long id) {

        List<Location> locations = null;
        
        try {
            locations = em.createNamedQuery("Location.getSupplierLocations", 
                Location.class)                
                    .setParameter("id", id)            
                .getResultList();

        } catch (Exception nre) {
            System.out.println("ERROR: "+nre);
        }

        return locations;
    }


}
